public class HurufZA {
    public static void main(String[] args) {
        System.out.println("Huruf dari Z hingga A:");
        char c = 'Z';  // Inisialisasi huruf mulai dari Z
        
        while (c >= 'A') {  // Kondisi selama c masih lebih besar atau sama dengan A
            System.out.print(c + " ");  // Cetak huruf saat ini
            c--;  // Decrement huruf
        }
    }
}


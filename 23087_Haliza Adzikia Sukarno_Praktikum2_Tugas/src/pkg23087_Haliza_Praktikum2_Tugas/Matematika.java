class Matematika implements MatematikaInterface {
    // Metode pertambahan
    public int pertambahan(int a, int b) {
        return a + b;
    }

    // Metode pengurangan
    public int pengurangan(int a, int b) {
        return a - b;
    }

    // Metode perkalian
    public int perkalian(int a, int b) {
        return a * b;
    }

    // Metode pembagian
    public int pembagian(int a, int b) {
        return a / b;
    }
}
